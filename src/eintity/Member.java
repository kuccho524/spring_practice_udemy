package eintity;

public class Member {
	
	// フィールドを宣言する
	private int id;
	private String name;
	private String email;
	
	// コンストラクタを定義
	public Member() {}

	public Member(int id, String name, String email) {
		this.id = id;
		this.name = name;
		this.email = email;
	}
	
	// getter, setterを定義
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	};
	

}
